# Difference between for loop and while loop

'''
for loop:
 - great for iteration of each item
 - Example 1: printing each item in a list
 
         fruits = ['Apple', 'Pear', 'Orange']
         for fruit in fruits:
             print(fruit)
             
 - Example 2: printing each number in the range
 
         for n in range (1, 6):
             print(n)


while loop:
 - great when you dont care what number in a sequence you are in, which item you are iterating in a list
 - if you want to peform a task repeatedly until the condition is satisfied
 - little more dangerous than for loops because for loops have a range whereas while loops do not.

'''