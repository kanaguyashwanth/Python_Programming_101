# NESTED LIST - Dirty Dozen

fruits = ['Strawberries', 'Nectarines', 'Apples', 'Grapes', 'Peaches', 'Cheeries', 'Pears']
vegetables = ['Spinach', 'Kale', 'Tomatoes', 'Celery', 'Potatoes']

dirty_dozen = [fruits, vegetables]

print (dirty_dozen)

print (dirty_dozen[0]) # Prints the first list, in this case
print (dirty_dozen[1]) # Prints the second list, in this case
print ('\n')

# Prints the list of items on the first list
print (dirty_dozen[0][0])
print (dirty_dozen[0][1])
print (dirty_dozen[0][2])
print (dirty_dozen[0][3])
print (dirty_dozen[0][4])
print (dirty_dozen[0][5])
print (dirty_dozen[0][6])
print ('\n')

# Prints the list of items on the second list
